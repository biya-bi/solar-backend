package org.rainbow.solar.exceptions;

public class PanelException extends SolarException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -1683644310253312324L;

	public PanelException() {
		super();
	}

	public PanelException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public PanelException(String message, Throwable cause) {
		super(message, cause);
	}

	public PanelException(String message) {
		super(message);
	}

	public PanelException(Throwable cause) {
		super(cause);
	}

}
